#bucle while

print("while con la sentencia break")
contador = 0
while contador < 10:
    contador += 1
    if contador == 5:
        break
    print("el valor actual de la variable  ", contador)
print("fin del programa\n")


#ejemplo continue
print("\nwhile con la sentencia break")
contador = 0
while contador < 10:
    contador += 1
    if contador == 5:
        continue  # en lugar de inmprimir el condicional 5 lo brinca
    print("el valor actual de la variable  ", contador)














